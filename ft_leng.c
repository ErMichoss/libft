#include "libft.h"

int	ft_leng(int n)
{
	int	j;
	int	i;
	int	z;

	j = 1;
	i = 1;
	z = 1;
	if (n < 0)
	{
		n = -n;
		z++;
	}
	while ((n / j) > 1)
	{
		j = j * 10;
		i++;
	}
	return (i + z);
}
